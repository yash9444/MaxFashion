let priceSort = document.querySelector("#price")

priceSort.addEventListener("change",function(){
    
    if(priceSort.value == "Ascending"){
        prodData.sort(function(a,b){
            if(a.price>b.price) return 1;
            if(a.price<b.price) return -1;
            return 0;
        })
        
        display(prodData)
    }
    if(priceSort.value == "Descending"){
        prodData.sort(function(a,b){
            if(a.price>b.price) return -1;
            if(a.price<b.price) return 1;
            return 0;
        })
        
        display(prodData)
        
    }
   
})


